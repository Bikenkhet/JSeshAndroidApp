package jsesh.mdc.constants;

/**
 * This class describes the various scripts available in the <em>manuel</em>.
 *   @author rosmord
 *
 */
public class ScriptCodes
{
	/**
	 * This code can be used to design all non-hieroglyphic fonts.
	 * In this case, derivations of the font will be used to provide italics, etc...
	 */
	static final public char ALL= '*';
	
	static final public char HIEROGLYPHIC= 's';
	static final public char LATIN= 'l';
	static final public char ITALIC= 'i';
	static final public char BOLD= 'b';
	static final public char TRANSLITERATION= 't';
	static final public char COPTIC= 'c';
	static final public char GREEK= 'g';
	static final public char HEBREW= 'h';
	static final public char CYRILLIC = 'c';
	static final public char COMMENT= '+';
}
